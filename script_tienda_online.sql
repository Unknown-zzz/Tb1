

-- 1. Usuarios
CREATE TABLE usuarios (
    id_usuario INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100),
    correo VARCHAR(100) UNIQUE,
    contrasena VARCHAR(255), -- Encriptada
    rol ENUM('cliente', 'admin', 'vendedor') DEFAULT 'cliente',
    activo BOOLEAN DEFAULT TRUE,
    creado_en TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. Direcciones
CREATE TABLE direcciones (
    id_direccion INT PRIMARY KEY AUTO_INCREMENT,
    id_usuario INT,
    direccion TEXT,
    ciudad VARCHAR(100),
    estado VARCHAR(100),
    codigo_postal VARCHAR(10),
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);

-- 3. Productos
CREATE TABLE productos (
    id_producto INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100),
    descripcion TEXT,
    precio DECIMAL(10,2),
    stock INT,
    creado_en TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 4. Categorías
CREATE TABLE categorias (
    id_categoria INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100)
);

-- 5. Producto-Categoría (relación muchos a muchos)
CREATE TABLE producto_categoria (
    id_producto INT,
    id_categoria INT,
    PRIMARY KEY (id_producto, id_categoria),
    FOREIGN KEY (id_producto) REFERENCES productos(id_producto),
    FOREIGN KEY (id_categoria) REFERENCES categorias(id_categoria)
);

-- 6. Órdenes
CREATE TABLE ordenes (
    id_orden INT PRIMARY KEY AUTO_INCREMENT,
    id_usuario INT,
    fecha TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    estado ENUM('pendiente', 'pagado', 'enviado', 'entregado', 'cancelado') DEFAULT 'pendiente',
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);

-- 7. Detalles de orden
CREATE TABLE detalles_orden (
    id_detalle INT PRIMARY KEY AUTO_INCREMENT,
    id_orden INT,
    id_producto INT,
    cantidad INT,
    precio_unitario DECIMAL(10,2),
    FOREIGN KEY (id_orden) REFERENCES ordenes(id_orden),
    FOREIGN KEY (id_producto) REFERENCES productos(id_producto)
);

-- 8. Métodos de pago
CREATE TABLE metodos_pago (
    id_pago INT PRIMARY KEY AUTO_INCREMENT,
    metodo ENUM('tarjeta', 'paypal', 'transferencia', 'efectivo', 'criptomoneda', 'pago móvil', 'vale de regalo', 'cheque', 'financiamiento', 'punto de pago', 'tarjeta de regalo', 'cupón de descuento', 'divisa', 'app bancaria', 'contrarreembolso', 'tarjeta prepago', 'transferencia instantánea', 'pago electrónico', 'pago en tienda', 'pago por teléfono', 'pago recurrente', 'pago con puntos', 'pago con wallet', 'pago con NFC', 'pago con código QR', 'pago con huella', 'pago facial', 'pago por email', 'pago en efectivo online', 'pago con tarjeta virtual', 'pago con biometría', 'pago con voz', 'pago con wearable', 'pago con tarjeta contactless', 'pago con tarjeta empresarial', 'pago con divisa digital', 'pago con seguro', 'pago con herencia', 'pago con donación', 'pago con beca', 'pago con patrocinio', 'pago con recompensa', 'pago con crowdfunding', 'pago con microcrédito', 'pago con nanocrédito', 'pago con crédito grupal', 'pago con crédito solidario', 'pago con ahorro previo', 'pago con fondos de inversión'),
    descripcion VARCHAR(100)
);

-- 9. Pagos
CREATE TABLE pagos (
    id_pago INT PRIMARY KEY AUTO_INCREMENT,
    id_orden INT,
    id_metodo_pago INT,
    fecha_pago TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    monto DECIMAL(10,2),
    FOREIGN KEY (id_orden) REFERENCES ordenes(id_orden),
    FOREIGN KEY (id_metodo_pago) REFERENCES metodos_pago(id_pago)
);

-- 10. Reseñas de productos
CREATE TABLE resenas (
    id_resena INT PRIMARY KEY AUTO_INCREMENT,
    id_usuario INT,
    id_producto INT,
    calificacion INT CHECK (calificacion BETWEEN 1 AND 5),
    comentario TEXT,
    fecha TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario),
    FOREIGN KEY (id_producto) REFERENCES productos(id_producto)
);

-- 11. Logs de acceso (parte de hardening)
CREATE TABLE logs_acceso (
    id_log INT PRIMARY KEY AUTO_INCREMENT,
    id_usuario INT,
    ip VARCHAR(45),
    exito BOOLEAN,
    fecha TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);

-- 12. Bitácora de acciones administrativas
CREATE TABLE bitacora_admin (
    id_bitacora INT PRIMARY KEY AUTO_INCREMENT,
    id_usuario INT,
    accion TEXT,
    fecha TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
);



